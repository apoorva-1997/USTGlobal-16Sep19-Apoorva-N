package com.ustglobal.typecasting;

public class Typecast {
	public static void main(String[] args) {  

		int p = 50;
		double q = p;					//implicit casting
		System.out.println(q);
		System.out.println(q);
		System.out.println("******************************************");
		double x = 20.9;
		int y =(int) x;					//explicit casting
		System.out.println(y);

	}
}