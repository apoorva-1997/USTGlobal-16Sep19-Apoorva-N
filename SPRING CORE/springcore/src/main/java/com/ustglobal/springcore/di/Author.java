package com.ustglobal.springcore.di;

public class Author {

	private String Name;
	private String PenName;
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPenName() {
		return PenName;
	}
	public void setPenName(String penName) {
		PenName = penName;
	};
	
	
}
